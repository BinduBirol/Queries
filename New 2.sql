  SELECT BILL_ID,
         DEDUCT_AMOUNT,
         DEDUCT_SURCHARGE,
         BANK_ID,
         BRANCH_ID,
         ACCOUNT_NO,
         TRANS_DATE
    FROM BILL_COLL_ADVANCED
   WHERE COLLECTED_BY = 'G-' || '1239784' AND BILL_ID IS NOT NULL
ORDER BY BILL_ID