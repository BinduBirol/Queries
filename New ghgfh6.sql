  SELECT DISTINCT
         (bi.CUSTOMER_ID), getBurner (bi.CUSTOMER_ID) BURNER, BI.Full_name
    FROM MVIEW_CUSTOMER_INFO bi, CUSTOMER_CONNECTION cc
   WHERE     BI.CUSTOMER_ID = CC.CUSTOMER_ID
         AND CC.STATUS = 1
         AND bi.area_id = '01'
         AND BI.CUSTOMER_ID BETWEEN '010100001' AND '010100100'
ORDER BY BI.CUSTOMER_ID ASC